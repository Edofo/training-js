// Objectif: Créez une fonction calculerAge(anneeNaissance) qui retourne l'âge d'une personne en 2025

const calculerAge=(anneeNaissance)=>{
    const anneeActuelle = 2025;
    return anneeActuelle - anneeNaissance;
};

